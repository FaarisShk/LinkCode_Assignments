
public class GreaterNumber {
	public static void greater(int n1,int n2) {
		if(n1>n2)
			System.out.println("Greater number is " + n1);
		else
			System.out.println("Greater number is " + n2);
	}  
	
	public static void greater(float f1,float	 f2) {
		if(f1>f2)
			System.out.println("Greater number is " + f1);
		else
			System.out.println("Greater number is " + f2);
	}
	
	public static void greater(char c1, char c2) {
		if(c1>c2)
			System.out.println("Greater character is " + c1);
		else 
			System.out.println("Greater character is " + c2);
	}
}
